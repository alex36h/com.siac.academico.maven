    private void Save() {
        try {
            x = new JPACrud();
            Pais values = new Pais();
            values.setNombre(this.JTNombre.getText());
            values.setActivo(this.Activo.isSelected());
            x.save(values);
            
            JOptionPane.showMessageDialog(this, ADD_OK);
            Refrescar();
            Clear();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, ADD_ERROR);
        }
    }

    private void Refrescar() {
        try {
            x = new JPACrud();
            model = getModel(COL_PAIS, false);
            limpiarJTable(model);

            List<Pais> entidad = x.buscarTodo(Pais.class);
            if (entidad != null) {
                for (Pais a
                        : entidad) {
                    model.addRow(new Object[]{a.getId(), a.getNombre().toUpperCase(),
                        getSiNo(a.getActivo())});
                }
            }
            jTable1.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }

