public class DetalleCierreCajaDenominaciones {
    private double deniminacion;
    private double cantidad;
    private double total;

    public double getDeniminacion() {
        return deniminacion;
    }

    public void setDeniminacion(double deniminacion) {
        this.deniminacion = deniminacion;
    }

    public double getCantidad() {
        return cantidad;
    }

    public void setCantidad(double cantidad) {
        this.cantidad = cantidad;
    }

    public double getTotal() {
        return total;
    }

    public void setTotal(double total) {
        this.total = total;
    }
}
